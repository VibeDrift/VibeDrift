import { describe, it, expect } from "vitest";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import { readFileSync } from "node:fs";
import { buildArmBlock } from "../src/arm-block.js";

describe("buildArmBlock", () => {
  const ownContext = "own repo context content";
  const placeboContext = "placebo repo context content";

  const expectedStart = "<!-- vibedrift:context:start (auto-generated, do not edit by hand) -->";
  const expectedEnd = "<!-- vibedrift:context:end -->";
  const expectedProvenance =
    "> The block below is VibeDrift's distilled view of this repo's dominant patterns and open drift. It is regenerated by `vibedrift --write-context --inject-context`. Read it before editing code here.";

  it("returns null for control arm (C)", () => {
    const result = buildArmBlock("C", ownContext, placeboContext);
    expect(result).toBeNull();
  });

  it("treatment arm (T) wraps own context with managed block markers", () => {
    const result = buildArmBlock("T", ownContext, placeboContext);
    expect(result).not.toBeNull();
    expect(result).toContain(expectedStart);
    expect(result).toContain(expectedEnd);
    expect(result).toContain(expectedProvenance);
    expect(result).toContain(ownContext);
    expect(result).not.toContain(placeboContext);
  });

  it("placebo arm (P) wraps placebo context with managed block markers", () => {
    const result = buildArmBlock("P", ownContext, placeboContext);
    expect(result).not.toBeNull();
    expect(result).toContain(expectedStart);
    expect(result).toContain(expectedEnd);
    expect(result).toContain(expectedProvenance);
    expect(result).toContain(placeboContext);
    expect(result).not.toContain(ownContext);
  });

  it("P and T have identical structure, differing only in context content", () => {
    const contentA = "AAAAA";
    const contentB = "BBBBB";

    const treatmentA = buildArmBlock("T", contentA, contentB);
    const placeboA = buildArmBlock("P", contentA, contentB);

    expect(treatmentA).not.toBeNull();
    expect(placeboA).not.toBeNull();

    // Extract the provenance + markers scaffold from both
    // T should have contentA, P should have contentB
    // Replace content in T with content from P and they should match
    const treatmentWithB = treatmentA!.replace(contentA, contentB);
    expect(treatmentWithB).toBe(placeboA);
  });

  it("managed block markers and provenance are exact byte matches from CLI", () => {
    const result = buildArmBlock("T", "test", "ignored");
    expect(result).toBeDefined();
    // Verify exact markers as per src/output/inject-context.ts
    expect(result).toContain(
      '<!-- vibedrift:context:start (auto-generated, do not edit by hand) -->',
    );
    expect(result).toContain("<!-- vibedrift:context:end -->");
  });

  it("block structure is START\\nprovenance\\n\\ncontent\\nEND", () => {
    const content = "test content";
    const result = buildArmBlock("T", content, "ignored");
    expect(result).not.toBeNull();

    // Verify the exact structure
    const expectedBlock = `${expectedStart}\n${expectedProvenance}\n\n${content}\n${expectedEnd}`;
    expect(result).toBe(expectedBlock);
  });
});

describe("drift-guard: arm-block markers must match shipped CLI", () => {
  it("verifies managed-block format literals match src/output/inject-context.ts", () => {
    // Guard: if this test fails, the shipped CLI changed its managed-block format
    // and src/arm-block.ts must be updated to match. Treatment fidelity depends on
    // exact byte-identity between the benchmark harness blocks and the CLI's output.
    const here = dirname(fileURLToPath(import.meta.url));
    const cliSrc = readFileSync(resolve(here, "../../../src/output/inject-context.ts"), "utf8");

    // Assert the four critical literals are present in the CLI source
    expect(cliSrc).toContain("<!-- vibedrift:context:start (auto-generated, do not edit by hand) -->");
    expect(cliSrc).toContain("<!-- vibedrift:context:end -->");
    expect(cliSrc).toContain(
      "The block below is VibeDrift's distilled view of this repo's dominant patterns and open drift."
    );
    expect(cliSrc).toContain(
      "It is regenerated by \\`vibedrift --write-context --inject-context\\`. Read it before editing code here."
    );
  });
});
