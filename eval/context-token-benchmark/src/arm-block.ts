import type { Arm } from "./types.js";

// NOTE: The markers and provenance text below MUST be kept byte-identical to
// src/output/inject-context.ts. A fidelity cross-check against the shipped CLI
// happens in Phase 2 (run-one / pilot); any divergence breaks arm comparability.
const MANAGED_BLOCK_START = "<!-- vibedrift:context:start (auto-generated, do not edit by hand) -->";
const MANAGED_BLOCK_END = "<!-- vibedrift:context:end -->";
const PROVENANCE = "> The block below is VibeDrift's distilled view of this repo's dominant patterns and open drift. It is regenerated by `vibedrift --write-context --inject-context`. Read it before editing code here.";

/**
 * Builds a per-arm CLAUDE.md managed block for the benchmark harness.
 *
 * - "C" (control) → null (no context block)
 * - "T" (treatment) → managed block wrapping ownContextMd
 * - "P" (placebo) → managed block wrapping placeboContextMd
 *
 * The block format matches the shipped CLI's inject-context output exactly,
 * so context blocks are token-comparable across arms.
 */
export function buildArmBlock(arm: Arm, ownContextMd: string, placeboContextMd: string): string | null {
  if (arm === "C") {
    return null;
  }

  const contextMd = arm === "T" ? ownContextMd : placeboContextMd;
  const body = `${PROVENANCE}\n\n${contextMd}`;
  return `${MANAGED_BLOCK_START}\n${body}\n${MANAGED_BLOCK_END}`;
}
